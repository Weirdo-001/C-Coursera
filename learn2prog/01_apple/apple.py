class Apple:
    def _init_(self, color, size, price):
        self.color = color
        self.size = size
        self.price = price

    def set_color(self, color):
        self.color = color

    def get_color(self):
        return self.color

    def set_size(self, size):
        self.size = size

    def get_size(self):
        return self.size

    def set_price(self, price):
        self.price = price

    def get_price(self):
        return self.price
